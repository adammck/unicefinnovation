<div id="content" class="with-sidebar">
	<div class="nest">
		<h2>The Issue</h2>
		<p>As youth spend more and more of their time online, the Internet has become their primary method of communication. Due to this, UNICEF's traditional methods of engaging and communicating with young people are losing their power and effectiveness. The geometric and global growth of social networking platforms provides an opportunity to create environments where UNICEF can not only communicate <strong>to</strong> youth but establish conversations <strong>with</strong> and <strong>between</strong> them. YouthNet is UNICEF's social network, that connects youth around the world with others concerned about the same interests and provides a safe environment for them to explore their ideas..</p>
		
		<h2>Action</h2>
		<p>YouthNet is a collection of social networks, each specifically for use by young people in addressing the issues that affect their worlds, be it HIV and AIDS activism or Climate Change. Each network provides the standard aspects of a social networking site as well as a number of useful tools for group work. The software was designed to work in areas that have low connectivity and be easy to use for any level of user.</p>

		<p>YouthNet's software facilitates relational networking by encouraging users to connect through shared concerns; groups form organically as users identify mutual interests and common goals. It supports advocacy and knowledge transfer through tools for collaborative work, including document creation, media sharing, polling and resource allocation and encourages users to take outcome-oriented action.</p>


		<p>YouthNet is Free Software, distributed under the GNU Public License, and written in Ruby on Rails..</p>
		
		<h2>Impact</h2>
		<p>There are unique challenges in each of the places UNICEF works. The knowledge to meet these challenges often exists in the communities, in local leaders and organizations. However the difficulty lies in identifying and aligning skills and resources with needs. Challenges are met when skills and resources come together. With social networking, individuals and groups are mapped out, allowing enhanced opportunities for knowledge transfer, task allocation, and resource mobilization. By bringing the power of collaborative networking to social challenges, UNICEF is empowering young people and organizations in the communities where they live and work.</p>


		<p>YouthNet is a tool that allows youth to have constructive conversations about their futures and organizations to extend their reach.</p>

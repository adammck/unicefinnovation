		<h2>The Issue</h2>
		<p>A consistent problem that UNICEF has faced when working with youth online, is that the tools for collaboration are not designed for them.  As UNICEf's programmatic work with youth focused more on collaboration, MediaWiki, the software that powers Wikipedia, became a commonly used tool.  In order to make it easier to use, the UNICEF Innovation team studied how youth in Africa used these tools, identified the largest barriers to use, and completely reworked it.  The resulting software is also used internally by UNCIEF staff and shared with the world allowing for anyone looking for a wiki tool to benefit from the research and work.</p>
		
		<h2>Action</h2>
		<p>UNIWIKI was designed to facilitate youth participation and collaboration around shared topics of interest.   The UNIWIKI software is a customized wiki that has been designed to be easy to use for young people, and people with limited experience using web applications.   UNIWIKI is a set of easy to install extensions for the MediaWiki software, and builds upon the open-source and collaborative nature of our work.  UNIWIKI allows anyone to create, edit, and share content with others.  It specifically makes creating new pages and adding images and attachments much easier.  The extensions also completely rework the editing interface to make it look and act like other commonly used software.</p>
		<p>In developing this platform much time and energy was put into making sure that it was easy for youth and first-time web users to interact with the software and perform basic tasks.  To accomplish this, UNICEF worked in conjunction with several youth groups around the world to "usability test" the system and incorporate their continuous feedback into the product.  In order to make sure that the software is available in as many areas as possible, special care has been taken to ensure that the UNIWIKI is lightweight and accessible under low-bandwidth and low-connectivity constraints.  Since its conception, the UNIWIKI extensions have been adopted by many groups around the world, and parts of it will soon be incorporated into the MediaWiki software itself.</p>
		
		<h2>Impact</h2>
		<p>Wiki's allow for collaboration between people everywhere on topics of shared interest. The UNIWIKI software makes such collaborations easier for young people and accessible even in low-bandwidth environments.</p>
		<p>The strength of such software was shown in UNICEF's Connecting Classrooms initiative.  This initiative involved students in Ethiopia, Germany, Uganda and the United States tasked with discussing and creating recommendations for how to improve the quality of education around the world. Not only did the software create an environment where youth were actively discussing and thinking about the issue, but they were also exploring each others cultures, overcoming stereotypes and finding shared values.  The best of the recommendations were presented to UNICEF's Education Division, providing them with the voice of those who are directly impacted by their work.</p>
		<p>As the youth of the world move increasingly online, tools such as UNIWIKI and the initiatives that use them will become even more vital to empower youth to collectively discuss and direct their own futures.</p>

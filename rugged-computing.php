		<h2>The Issue</h2>
		<p>UNICEF is always looking for tools and methods for improving it's emergency responses in crisis situations.  The Bee is designed to provide a computing and communications infrastructure during an emergency.  For such responses to be successful, it is essential that UNICEF and other relief organizations have access to reliable information both during and immediately after the crisis. UNICEF Innovations sought to create a diverse communications system that is easily transportable, powered in a variety of ways and able to withstand adverse environmental conditions. Constructed with commonly available components and with blueprints online, they are easy and cheap to repair and outside organizations can create their own Bee.</p>
		
		<h2>Action</h2>
		<p>The Bee is a mobile computing system that allows communication, connectivity and data access in field conditions where such technologies are often difficult or impossible to use. As access to both power and connectivity are limiting factors in many emergency and field settings, the Bee has been built with these limitations in mind. It can be charged with solar power, a car battery, or a conventional power source and can use WiFi, GSM Networks, a FM radio transmitter, or a satellite uplink to communicate.</p>
		<p>Two varieties of Bees, Worker Bees and Queen Bees, are deployed together to form a peer-to-peer communication network. In addition to a low-power computer terminal, each Worker Bee is equipped with a web-cam, projector, speakers, GSM modem, FM transmitter (50km range), and directional WiFi (5km range). Queen Bees also boast satellite links and higher-powered directional WiFi, allowing the Queen to push and pull data to and from the Internet, and to aggregate and distribute information among Workers.</p>
		<p>Custom administration software, known as Beehive, coordinates data automatically among the Bees, such as names and photos for connecting displaced families. Each Bee acts as a community radio station, WiFi access point, school, and Internet kiosk.</p>
		<p>The Bee hardware designs are freely available and can be constructed entirely with off-the-shelf consumer components, save for the casing. These components are powered by free and open-source software designed to support the efforts of field workers and partners, and to be locally adapted for ongoing use. The Bees are ruggedized, able to withstand extreme temperature variations, rain, and dust. Furthermore, the Bee is light enough to be transported by one person and can be checked on commercial airlines.</p>
		
		<h2>Impact</h2>
		<p>The Bee can strengthen UNICEF's ability to respond to emergencies. This lightweight computer and communications system can:</p>
		<ul>
			<li>be deployed quickly</li>
			<li>run off of a large variety of power sources</li>
			<li>be rugged enough to survive harsh weather and environmental conditions</li>
			<li>create a network for other communication equipment to run on</li>
		</ul>
		<p>It is an invaluable tool that can serve a multitude of purposes from using the radio to broadcast emergency announcements to using the camera to photograph family members who are looking for lost loved ones.</p>
		<p>Additionally, the system is designed to work within UNICEF’s ‘build back better’ policy of helping families and communities recover from emergencies. While the components of the Bee are intended initially to facilitate crisis-response operations, the machines will left in the communities they are deployed in as a computing kiosk.  They can become integrated into the community’s ongoing monitoring, evaluation, education and health programmes.</p>
